// schema/gen generates the schema package from a columns.txt export.
//
// # Generate columns.txt from MariaDB
//
//	mysql -u <user> -p <dbname> -e "
//	  SELECT TABLE_NAME, COLUMN_NAME, COLUMN_TYPE, IS_NULLABLE
//	  FROM information_schema.COLUMNS
//	  WHERE TABLE_SCHEMA = '<dbname>'
//	  ORDER BY TABLE_NAME, ORDINAL_POSITION;" > columns.txt
//
// # Regenerate schema/tables.go
//
//	go run ./schema/gen columns.txt schema/tables.go
//
// # Exclude tables
//
// Pass --exclude with a file listing tables to skip (one per line, # for comments):
//
//	go run ./schema/gen --exclude exclude.txt columns.txt schema/tables.go
package main

import (
	"bufio"
	"flag"
	"fmt"
	"os"
	"strings"
	"unicode"
)

type column struct {
	table    string
	name     string
	dbType   string
	nullable bool
}

func main() {
	excludeFile := flag.String("exclude", "", "file listing tables to exclude (one per line, # for comments)")
	flag.Usage = func() {
		fmt.Fprintln(os.Stderr, "usage: schema/gen [--exclude exclude.txt] <columns.txt> <output.go>")
		flag.PrintDefaults()
	}
	flag.Parse()

	if flag.NArg() != 2 {
		flag.Usage()
		os.Exit(1)
	}

	excluded := loadExcludeList(*excludeFile)
	cols, tables := parse(flag.Arg(0), excluded)

	f, err := os.Create(flag.Arg(1))
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
	defer f.Close()

	w := bufio.NewWriter(f)
	emit(w, cols, tables)
	w.Flush()
}

// loadExcludeList reads a file of table names to exclude (one per line, # for comments).
// Returns an empty map if path is empty.
func loadExcludeList(path string) map[string]bool {
	excluded := map[string]bool{}
	if path == "" {
		return excluded
	}
	f, err := os.Open(path)
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
	defer f.Close()

	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}
		excluded[line] = true
	}
	return excluded
}

// parse reads columns.txt and returns columns + ordered table list (no views, no excluded tables).
func parse(path string, excluded map[string]bool) ([]column, []string) {
	f, err := os.Open(path)
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
	defer f.Close()

	var cols []column
	seen := map[string]bool{}
	var tables []string

	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := scanner.Text()
		parts := strings.Split(line, "\t")
		if len(parts) < 4 {
			continue
		}
		tbl := parts[0]
		col := parts[1]
		typ := parts[2]
		nullable := parts[3] == "YES"

		// skip header, views, and explicitly excluded tables
		if tbl == "TABLE_NAME" || strings.HasPrefix(tbl, "v_") || excluded[tbl] {
			continue
		}

		if !seen[tbl] {
			seen[tbl] = true
			tables = append(tables, tbl)
		}
		cols = append(cols, column{tbl, col, typ, nullable})
	}
	return cols, tables
}

func emit(w *bufio.Writer, cols []column, tables []string) {
	fmt.Fprintln(w, "// Code generated by schema/gen. DO NOT EDIT.")
	fmt.Fprintln(w, "// Regenerate with: go run ./schema/gen columns.txt schema/tables.go")
	fmt.Fprintln(w, "")
	fmt.Fprintln(w, "package schema")
	fmt.Fprintln(w, "")

	// Table vars
	fmt.Fprintln(w, "// Tables")
	fmt.Fprintln(w, "var (")
	for _, t := range tables {
		fmt.Fprintf(w, "\t%s = &Table{Name: %q}\n", tableVar(t), t)
	}
	fmt.Fprintln(w, ")")
	fmt.Fprintln(w, "")

	// Column vars, grouped by table
	for _, t := range tables {
		fmt.Fprintf(w, "// Columns of %s\n", t)
		fmt.Fprintln(w, "var (")
		for _, c := range cols {
			if c.table != t {
				continue
			}
			fmt.Fprintf(w, "\t%s = &Col{T: %s, Name: %q, Nullable: %v}\n",
				colVar(c.table, c.name), tableVar(t), c.name, c.nullable)
		}
		fmt.Fprintln(w, ")")
		fmt.Fprintln(w, "")
	}

	// AllCols registry — used by the query builder for join resolution
	fmt.Fprintln(w, "// AllCols is the full column registry used for join resolution.")
	fmt.Fprintln(w, "var AllCols = []*Col{")
	for _, c := range cols {
		fmt.Fprintf(w, "\t%s,\n", colVar(c.table, c.name))
	}
	fmt.Fprintln(w, "}")
}

// tableVar returns the Go variable name for a table: "nodes" → "TNodes".
func tableVar(table string) string {
	return "T" + pascal(table)
}

// colVar returns the Go variable name for a column: ("nodes","node_id") → "NodesNodeID".
func colVar(table, col string) string {
	return pascal(table) + pascal(col)
}

// pascal converts snake_case to PascalCase, with common acronym handling.
func pascal(s string) string {
	parts := strings.Split(s, "_")
	var b strings.Builder
	for _, p := range parts {
		if len(p) == 0 {
			continue
		}
		up := strings.ToUpper(p)
		if acronyms[up] {
			b.WriteString(up)
		} else {
			runes := []rune(p)
			runes[0] = unicode.ToUpper(runes[0])
			b.WriteString(string(runes))
		}
	}
	return b.String()
}

// acronyms lists abbreviations that should stay fully uppercase.
var acronyms = map[string]bool{
	"ID":  true,
	"IP":  true,
	"URL": true,
	"CPU": true,
	"MEM": true,
	"OS":  true,
	"FK":  true,
	"MD5": true,
	"HBA": true,
	"SAN": true,
	"DRP": true,
	"DG":  true,
	"PW":  true,
	"HW":  true,
}
